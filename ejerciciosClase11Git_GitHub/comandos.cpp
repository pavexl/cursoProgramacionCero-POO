/*
========================= COMANDOS ESPECIALES DE GIT Y GITHUB ===============================

1. git add
    preparar los cambios para poder sincronizarlos

2. git commit -m "mensaje"
    registrar un avance

3. git push
    sube a GitHum los cambios locales (compu -> nube)

4. git pull 
    trae cambios desde GitHub a la computadora (nube -> compu) 
*/